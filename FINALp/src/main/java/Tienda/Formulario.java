    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        if(this.ID.getText().trim().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Ingrese el codigo de identificaci贸n del producto");
            ID.setBackground(Color.red);
            ID.requestFocus();
            }else if(this.DESCRIPCION.getText().trim().length()==0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese una descripci贸n del producto");
        DESCRIPCION.setBackground(Color.red);
        DESCRIPCION.requestFocus();
            }else if(this.PRECIO.getText().trim().length()==0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese precio del producto");
        PRECIO.setBackground(Color.red);
        PRECIO.requestFocus();
            }else if(this.CANTIDAD.getText().trim().length()==0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese la cantidad");
        CANTIDAD.setBackground(Color.red);
        CANTIDAD.requestFocus();
            }else if(this.FECHA.getText().trim().length()==0){
        JOptionPane.showMessageDialog(rootPane, "Ingrese la fecha");
        FECHA.setBackground(Color.red);
        FECHA.requestFocus();
            }else{
                guardar(ID.getText(),
                DESCRIPCION.getText(),
                FECHA.getText(),
                Integer.parseInt(CANTIDAD.getText()),
                Float.parseFloat(PRECIO.getText())
                );
        calculo(Float.parseFloat(PRECIO.getText()), Integer.parseInt(CANTIDAD.getText()));
        limpiar();
            }
    }//GEN-LAST:event_guardarActionPerformed

    private void sumaTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sumaTotalActionPerformed
    double suma, sumatoria=0;
    int totalrow=tabla.getRowCount();
    totalrow-=1;
    for(int i=0; i<=(totalrow); i++){
    suma=Double.parseDouble(String.valueOf(tabla.getValueAt(i,6)));
    sumatoria+=suma;
    }
    total.setText(String.valueOf(sumatoria));
    }//GEN-LAST:event_sumaTotalActionPerformed

    private void efectivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efectivoActionPerformed
    float total=0, efectivo=0, cambio=0, tarjeta=0;
    total=Float.parseFloat(this.total.getText());
    efectivo=Float.parseFloat(JOptionPane.showInputDialog(null, "Ingrese un valor en efectivo mayor o igual a: "+total));
    if (efectivo>=total) {
        cambio=efectivo-total;
    } else {
        do {                
            efectivo=Float.parseFloat(JOptionPane.showInputDialog(null, "Ingrese un valor en efectivo mayor o igual a: "+total));
        } while (efectivo<=efectivo-total);
        cambio=efectivo-total;
    }
    JOptionPane.showMessageDialog(null, """
                                         ------- FACTURA ------- 
                                         EFECTIVO \t"""+efectivo
                            + "\n TARJETA "+tarjeta
                            + "\n CAMBIO "+cambio);
    }//GEN-LAST:event_efectivoActionPerformed

