    public void guardar(String id, String descripcion, String fecha, int cantidad, float precio){
        float total=0, iva=0, subtotal;
        DefaultTableModel tabla=(DefaultTableModel)this.tabla.getModel();
        JOptionPane.showMessageDialog(null, "Datos guardados con exito");
        subtotal=cantidad*precio;
        iva=subtotal*0.12f;
        total=subtotal+iva;
        Object[]obj = {id, descripcion, fecha, precio, cantidad, iva, total};
        tabla.addRow(obj);
    }
    
